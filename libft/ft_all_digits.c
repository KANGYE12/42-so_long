#include "libft.h"

int	ft_all_digits(char *str)
{
	int		i;

	i = 0;
	if (str[0] == '-' || str[0] == '+')
		i++;
	while (str[i] != '\0')
	{
		if (ft_isdigit(str[i]) == 0 || str[i] == ' ')
			return (0);
		i++;
	}
	return (1);
}
