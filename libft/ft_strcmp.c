int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	solution;

	i = 0;
	solution = 0;
	while ((s1[i] != '\0' && s2[i] != '\0') && (s1[i] == s2[i]))
	{
		i++;
	}
	solution = s1[i] - s2[i];
	return (solution);
}
